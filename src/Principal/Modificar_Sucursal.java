public void CargarBanco(){
    
int IDMAX = 0;



try{
    

resultado = Conexion.consulta("Select MAX([Codigo_Banco]) from [dbo].[Banco]");

while(resultado.next()){
  
    IDMAX = resultado.getInt(1);
    
}

}catch(SQLException Ex){
    
}

ID_Banco = new int[IDMAX+1];

ID_Banco [0] = 0;

int i = 1;

try{
    

resultado = Conexion.consulta("Select [Codigo_Banco], [Nombre] "
        + " from [dbo].[Banco] where [Estado] = 1");

while(resultado.next()){
  
    ID_Banco[i] = resultado.getInt(1);
    CmbBanco.addItem(resultado.getString(2));
    i++;
}

}catch(SQLException Ex){
    
}
}
  

